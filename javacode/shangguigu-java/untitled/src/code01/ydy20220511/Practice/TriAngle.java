package code01.ydy20220511.Practice;

public class TriAngle {

    private int base;//边长
    private int height;//高

    public  TriAngle(int base,int height){

        this.base=base;
        this.height=height;
    }

    public TriAngle(){


    }
    public int getBase() {

        return base;
    }


    public void setBase(int base) {

        this.base = base;
    }

    public int getHeight() {

        return height;
    }

    public void setHeight(int height) {

        this.height = height;
    }
}
