package java_api_study.Math_api;

/*
*
* Math类
*
*
* */

public class Demo {
    public static void main(String[] args) {

        System.out.println(Math.PI);//圆周率
        System.out.println(Math.abs(-10));//绝对值
        System.out.println(Math.max(10, 20));//最大值
        System.out.println(Math.min(10, 20));//最小值
        System.out.println(Math.floor(10.9));//向下取整
        System.out.println(Math.ceil(10.001));//向上取整
        System.out.println(Math.round(10.5));//四舍五入

    }
}
